#Operadores de comparação
    #São operadores utilizados para comparar dois valores.

#Igualdade

saldo = 450
saque = 200

print(saldo == saque) #Retorna False

#Diferença

print(saldo != saque) #Retorna True

#Maior que / Maior ou igual que

print(saldo > saque) #Retorna True
print(saldo >= saque) #Retorna True

#Menor que / Menor ou igual que

print(saldo < saque) #Retorna False
print(saldo <= saque) #Retorna False