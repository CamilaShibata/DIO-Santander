#Operadores de atribuição
    #São operadores utilizados para definir o valor inicial ou sobrescrever o valor de uma variável.

#Atribuição simples

saldo = 500 #Atribuindo o valor 500 para a variável saldo.
print(saldo) #Retorna 500

#Atribuição com adição

saldo = 500
saldo += 200
print(saldo) #Retorna 700

#Atribuição com subtração

saldo = 500
saldo -= 100
print(saldo) #Retorna 400

#Atribuição com multiplicação

saldo = 500
saldo *= 2
print(saldo) #Retorna 1000

#Atribuição com divisão e divisão inteira

saldo = 500
saldo /= 5
print(saldo) #Retorna 100.0

saldo = 500
saldo //= 5
print(saldo) #Retorna 100

#Atribuição com módulo

saldo = 500
saldo %= 480
print(saldo) #Retorna 20

#Atribuição com exponenciação

saldo = 80
saldo **= 2
print(saldo) #Retorna 6400